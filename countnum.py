# สร้างรายการเก็บจำนวนการป้อนเลข 0-9
count = [0] * 10

while True:
    # รับเลขจำนวนเต็มจากผู้ใช้
    num = int(input("ป้อนเลข 0-9: "))
    
    # ตรวจสอบว่าเลขที่ป้อนอยู่ในช่วง 0-9 หรือไม่
    if 0 <= num <= 9:
        # เพิ่มจำนวนการป้อนเข้าไปในรายการ count
        count[num] += 1
    else:
        print("เลขที่ป้อนไม่ถูกต้อง")
        break

# แสดงผลลัพธ์
for i in range(10):
    print(f"{i}= {count[i]}")